const removedFeaturePattern = new RegExp([
  '集成中心', 'Integration Center',
  '生产验收', 'production acceptance', 'production readiness', 'production proof workspace', 'production proof output', 'live acceptance report',
  '用户生命周期', 'user lifecycle',
  '秒杀', 'flash[ -]?sales?',
  '资源风控', 'resource[ -]?risk', '风控',
  '交付保障', 'delivery[ -]?assurance',
  '主题中心', '主题市场', '主题系统', 'theme[ -]?(center|market|system|package|manifest)',
  '扩展中心', '扩展市场', '扩展平台', '插件', '\\bplugins?\\b', '\\bextensions?\\b',
  'AI\\s*工单', '工单\\s*AI', 'AI ticket', 'ticket AI',
  '\\bSLA\\b', '告警中心', 'alert center',
  '容量与成本', 'capacity[ -]?(and[ -])?cost',
  '交易所', '兑换市场'
].join('|'), 'i')

function isRemovedFeatureLine(value) {
  if (removedFeaturePattern.test(value)) return true
  if (/\bexchange\b/i.test(value) && !/(OAuth|authorization|token|currency)/i.test(value)) return true
  if (/(争议|交割|挂牌|买家|卖家)/.test(value)) return true
  if (/(buyer|seller|listing|escrow|dispute).*(instance|order|delivery|market|refund)|delivery model.*buyers/i.test(value)) return true
  if (/theme (asset|resource).*(CSP|sandbox|XSS)/i.test(value)) return true
  return false
}

function removeEmptyHeadings(lines) {
  let changed = true
  while (changed) {
    changed = false
    for (let index = 0; index < lines.length; index += 1) {
      if (!/^#{3,6} /.test(lines[index])) continue
      let next = index + 1
      while (next < lines.length && !lines[next].trim()) next += 1
      if (next >= lines.length || /^#{2,6} /.test(lines[next])) {
        lines.splice(index, 1)
        changed = true
        break
      }
    }
  }
  return lines
}

function git(args) {
  return execFileSync('git', args, {
    cwd: repoRoot,
    encoding: 'utf8',
    stdio: ['ignore', 'pipe', 'pipe']
  }).trim()
}

function safeGit(args, fallback = '') {
  try {
    return git(args)
  } catch {
    return fallback
  }
}

function listTags() {
  const output = safeGit(['tag', '--list', 'v*', '--sort=-v:refname'])
  return output ? output.split('\n').filter(Boolean) : []
}

function commitMeta(ref) {
  const line = safeGit(['log', '-1', '--format=%H%x09%cs%x09%s', ref])
  const [fullHash = '-', date = '-', subject = '-'] = line.split('\t')
  const shortHash = fullHash === '-' ? fullHash : fullHash.slice(0, 9)
  return { shortHash, date, subject: normalizeProductWording(subject) }
}

function commitsBetween(fromRef, toRef) {
  const range = fromRef ? `${fromRef}..${toRef}` : toRef
  const output = safeGit(['log', '--format=%H%x09%s', range])
  if (!output) return []
  return output
    .split('\n')
    .map((line) => {
      const [fullHash, ...subjectParts] = line.split('\t')
      return { hash: fullHash.slice(0, 9), subject: normalizeProductWording(subjectParts.join('\t')) }
    })
    .filter((item) => item.hash && item.subject)
}

function isDocumentationSyncCommit(subject) {
  return /\b(handoff|version log)\b/i.test(subject) || /^Release v\d+\.\d+\.\d+/i.test(subject)
}

function tagContent(tag, format) {
  return safeGit(['tag', '--list', tag, `--format=${format}`]).trim()
}

function normalizeTagNote(value) {
  return value.replace(/\\n/g, '\n')
}

function normalizeProductWording(value) {
  return value
    .replace(/插件中心/g, '扩展中心')
    .replace(/\bPlugin Center\b/g, 'Extension Center')
    .replace(/\bPlugin center\b/g, 'Extension center')
    .replace(/\bplugin center\b/g, 'extension center')
}

function normalizeEmbeddedReleaseNoteHeadings(lines, tag) {
  return lines.flatMap((line) => {
    const heading = line.match(/^(#{1,6})\s+(.+)$/)
    if (!heading) return [line]

    const [, hashes, title] = heading
    if (hashes.length === 1 && title.trim().toLowerCase() === tag.toLowerCase()) {
      return []
    }

    const level = Math.min(6, Math.max(3, hashes.length + 1))
    return [`${'#'.repeat(level)} ${title}`]
  })
}

function tagBodyLines(tag) {
  const fullContent = normalizeTagNote(tagContent(tag, '%(contents)'))
  const body = normalizeTagNote(tagContent(tag, '%(contents:body)'))
  const firstLine = fullContent.split(/\r?\n/).find((line) => line.trim())
  const startsWithGroupHeading =
    firstLine && /\/ (Fixes and stability|New capabilities|Other changes|Improvements and adjustments|修复与稳定性|新增能力|其他变更|改进与调整)/i.test(firstLine.trim())
  const note = startsWithGroupHeading ? fullContent : (body || fullContent.split(/\r?\n/).slice(1).join('\n'))
  if (!note) return []

  const lines = []
  for (const rawLine of note.split(/\r?\n/)) {
    const line = normalizeProductWording(rawLine.trim())
    if (isRemovedFeatureLine(line)) continue
    if (!line) {
      if (lines.length > 0 && lines[lines.length - 1] !== '') lines.push('')
      continue
    }
    if (!line.startsWith('- ') && / \/ /.test(line)) {
      lines.push(`### ${line.replace(/:$/, '')}`)
      lines.push('')
    } else {
      lines.push(line)
    }
  }

  removeEmptyHeadings(lines)
  while (lines[lines.length - 1] === '') lines.pop()
  return lines.length > 0 ? [...lines, ''] : []
}

function releaseNoteOverrideLines(tag) {
  const filePath = resolve(releaseNotesDir, `${tag}.md`)
  if (!existsSync(filePath)) return []

  const content = readFileSync(filePath, 'utf8')
  const lines = []
  for (const rawLine of content.split(/\r?\n/)) {
    const line = normalizeProductWording(rawLine.trimEnd())
    if (isRemovedFeatureLine(line)) continue
    if (!line.trim()) {
      if (lines.length > 0 && lines[lines.length - 1] !== '') lines.push('')
      continue
    }
    lines.push(line)
  }

  removeEmptyHeadings(lines)
  while (lines[lines.length - 1] === '') lines.pop()
  return lines.length > 0 ? [...lines, ''] : []
}

function classify(subject, locale) {
  const text = subject.toLowerCase()
  if (/(fix|repair|rollback|tolerate|stabilize|harden)/.test(text)) return locale.groups.fixes
  if (/(add|support|allow|enable|create)/.test(text)) return locale.groups.features
  if (/(update|rewrite|prefer|make|prepare|improve|refactor)/.test(text)) return locale.groups.improvements
  return locale.groups.other
}

function groupedCommitLines(commits, locale) {
  if (commits.length === 0) return [locale.duplicateTag, '']

  const groups = new Map()
  for (const commit of commits) {
    if (isRemovedFeatureLine(commit.subject)) continue
    const key = classify(commit.subject, locale)
    const items = groups.get(key) ?? []
    items.push(commit)
    groups.set(key, items)
  }

  const lines = []
  for (const [group, items] of groups) {
    lines.push(`### ${group}`)
    lines.push('')
    for (const item of items) {
      lines.push(`- ${item.subject} \`${item.hash}\``)
    }
    lines.push('')
  }
  return lines
}

function render() {
  const tags = listTags()
  const latestTag = tags[0]
  const latestRelease = latestTag ? commitMeta(latestTag) : commitMeta('HEAD')
  for (const locale of locales) {
  const lines = [
    `# ${locale.title}`,
    '',
    locale.generatedComment,
    '',
    locale.intro,
    '',
    ...(locale.originalLanguageNotice ? [locale.originalLanguageNotice, ''] : []),
    `## ${locale.currentState}`,
    '',
    `- ${locale.currentHead}: \`${latestRelease.shortHash}\``,
    `- ${locale.commitDate}: ${latestRelease.date}`,
    ...(!isRemovedFeatureLine(latestRelease.subject) ? [`- ${locale.commitSubject}: ${latestRelease.subject}`] : []),
    `- ${locale.latestTag}: ${latestTag ? `\`${latestTag}\`` : locale.noTag}`,
    '',
    `## ${locale.unreleased}`,
    ''
  ]

  const unreleased = latestTag
    ? commitsBetween(latestTag, 'HEAD').filter((commit) => !isDocumentationSyncCommit(commit.subject))
    : []
  lines.push(...groupedCommitLines(unreleased, locale))

  lines.push(`## ${locale.history}`, '')

  if (tags.length === 0) {
    lines.push(locale.noTags)
  }

  for (let index = 0; index < tags.length; index += 1) {
    const tag = tags[index]
    if (removedFeatureOnlyTags.has(tag)) continue
    const nextTag = tags[index + 1]
    const meta = commitMeta(tag)
    const commits = commitsBetween(nextTag, tag).filter((commit) => !isDocumentationSyncCommit(commit.subject))

    lines.push(`## ${tag}`, '')
    lines.push(`- ${locale.releaseCommit}: \`${meta.shortHash}\``)
    lines.push(`- ${locale.commitDate}: ${meta.date}`)
    if (!isRemovedFeatureLine(meta.subject)) lines.push(`- ${locale.commitSubject}: ${meta.subject}`)
    lines.push('')
    const releaseNotes = releaseNoteOverrideLines(tag)
    const fallbackReleaseNotes = tagBodyLines(tag)
    if (releaseNotes.length > 0) {
      lines.push(...normalizeEmbeddedReleaseNoteHeadings(releaseNotes, tag))
    } else if (fallbackReleaseNotes.length > 0) {
      lines.push(...normalizeEmbeddedReleaseNoteHeadings(fallbackReleaseNotes, tag))
    } else {
      lines.push(...groupedCommitLines(commits, locale))
    }
  }

  lines.push(`## ${locale.generation}`, '')
  lines.push(locale.runBuild, '')
  lines.push('```bash')
  lines.push('pnpm docs:build')
  lines.push('```')
  lines.push('')
  lines.push(locale.runChangelog, '')
  lines.push('```bash')
  lines.push('pnpm docs:changelog')
  lines.push('```')
  lines.push('')
  lines.push(locale.shallowClone)

  mkdirSync(dirname(locale.outputFile), { recursive: true })
  writeFileSync(locale.outputFile, `${lines.join('\n')}\n`, 'utf8')
  console.log(`generated ${locale.outputFile}`)
  }
}

render()
